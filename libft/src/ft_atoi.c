#include "libft.h"

int		ft_atoi(const char *str)
{
	int number;
	int sign;
	int i;

	number = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\f'
		|| str[i] == '\v' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10) + (str[i] - '0');
		if (number > 2147483647 || number * sign < -2147483648)
			return (0);
		i++;
	}
	return (number * sign);
}
